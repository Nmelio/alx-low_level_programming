#include <stdio.h>

/**
 * add - adds var a and var b
 * @a: first variable
 * @b: second variable
 * Return: the answer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substracts var a from var b
 * @a: first variable
 * @b: second variable
 * Return: the answer
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies var a and var b
 * @a: first variable
 * @b: second variable
 * Return: the answer
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides var a by var b
 * @a: first variable
 * @b: second variable
 * Return: the answer
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - divides var a by var b amd returns the reminder
 * @a: first variable
 * @b: second variable
 * Return: the answer
 */
int mod(int a, int b)
{
	return (a % b);
}
